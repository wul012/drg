#pragma once
#include <vector>

// ============================================================================
//                    硬币问题 —— 新手友好详解
// ============================================================================
//
// 【问题背景】
//   给定一组硬币面值和目标金额，求凑成该金额所需的最少硬币数。
//   如果无法凑成，返回 -1。
//
// 【暴力解法的问题】
//   暴力枚举所有可能的硬币组合，时间复杂度指数级，太慢。
//
// 【动态规划的优势】
//   利用子问题的重叠性，避免重复计算，时间复杂度降到 O(n * amount)。
//
// 【核心思想】
//   1) 递归思路：对于每个硬币，我们有两种选择：
//      - 不使用当前硬币，考虑下一个硬币
//      - 使用当前硬币（如果面值不超过剩余金额），剩余金额减少
//
//   2) 状态定义：
//      - process(arr, index, rest) 表示用 arr[index...] 的硬币凑成 rest 的最少硬币数
//      - 返回 -1 表示无法凑成
//
//   3) 状态转移：
//      - 如果 rest < 0：无效状态，返回 -1
//      - 如果 rest == 0：已凑够，返回 0
//      - 如果 index == arr.length：没有硬币可用，返回 -1
//      - 否则：取 "不用当前硬币" 和 "用当前硬币" 两种方案的较小值
//
// 【关键变量说明】
//   - arr[]: 硬币面值数组
//   - index: 当前考虑的硬币索引
//   - rest: 剩余需要凑的金额
//
// ============================================================================

namespace coins_min {
    // 暴力递归方法：求用 arr 中的硬币凑成 amount 的最少硬币数
    // 返回 -1 表示无法凑成
    // 例如：minCoinsRecursive({1, 2, 5}, 11) 返回 3（5+5+1）
    int minCoinsRecursive(const std::vector<int>& arr, int amount);

    // 动态规划方法：同上，但使用记忆化避免重复计算
    // 时间复杂度 O(n * amount)，空间复杂度 O(n * amount)
    int minCoinsDp(const std::vector<int>& arr, int amount);
}
