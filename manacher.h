#pragma once
#include <string>
#include <vector>

// ============================================================================
//                    Manacher 马拉车算法 —— 新手友好详解
// ============================================================================
//
// 【问题背景】
//   给定一个字符串，找出其中最长的回文子串。
//   回文串：正着读和倒着读一样的字符串，如 "aba"、"abba"、"12321"。
//
// 【暴力解法的问题】
//   暴力枚举每个中心点向两边扩展，时间复杂度 O(N²)，对于长字符串太慢。
//
// 【马拉车算法的优势】
//   利用回文串的对称性，避免重复计算，时间复杂度降到 O(N)。
//
// 【核心思想】
//   1) 预处理：在每个字符间插入分隔符 '#'
//      - 目的：统一处理奇数和偶数长度的回文串
//      - 例如："aba"  -> "#a#b#a#"  （奇数长度）
//      - 例如："abba" -> "#a#b#b#a#"（偶数长度）
//      - 处理后，所有回文串长度都变成奇数！
//
//   2) 利用对称性加速：
//      - 维护一个"最右回文边界" R 和对应的中心 C
//      - 当计算新位置 i 的回文半径时，若 i 在 R 内，
//        可以利用 i 关于 C 的对称点 i' 的信息来加速
//
// 【关键变量说明】
//   - pArr[i]：位置 i 的回文半径（包括自身）
//   - C：当前最右回文的中心位置
//   - R：当前最右回文的右边界（不包含），即最右有效位置是 R-1
//
// ============================================================================

namespace manacher {
    // 将原串转换为带分隔符的形式。
    // 例如："1221" -> "#1#2#2#1#"
    // 例如："abc"  -> "#a#b#c#"
    // 例如：""     -> "#"
    std::string manacherString(const std::string& str);

    // 计算字符串 s 的最长回文子串长度。复杂度 O(N)。
    // 约定：当 s 为空时返回 0。
    // 例如：maxLcpsLength("babad") 返回 3（"bab" 或 "aba"）
    int maxLcpsLength(const std::string& s);

    // 获取最长回文子串本身。
    // 例如：longestPalindrome("babad") 返回 "bab" 或 "aba"
    std::string longestPalindrome(const std::string& s);
}
