#pragma once
#include <vector>
#include <functional>

// ============================================================================
//                    Morris 遍历算法 —— 新手友好详解
// ============================================================================
//
// 【问题背景】
//   二叉树的遍历（前序、中序、后序）通常需要：
//   - 递归方式：使用系统栈，空间复杂度 O(h)，h 是树高
//   - 迭代方式：使用显式栈，空间复杂度 O(h)
//   
//   Morris 遍历可以做到 O(1) 额外空间复杂度！
//
// 【核心思想】
//   利用叶子节点的空闲指针（null 指针）来保存返回信息，
//   遍历完成后再恢复树的结构。
//
// 【Morris 遍历的规则】
//   设当前节点为 cur，初始时 cur = head（根节点）
//   
//   1. 如果 cur 没有左子树：
//      - cur 直接向右移动：cur = cur->right
//   
//   2. 如果 cur 有左子树：
//      - 找到左子树的最右节点 mostRight
//      
//      a) 如果 mostRight->right == nullptr（第一次到达 cur）：
//         - 让 mostRight->right = cur（建立返回路径）
//         - cur 向左移动：cur = cur->left
//      
//      b) 如果 mostRight->right == cur（第二次到达 cur）：
//         - 恢复：mostRight->right = nullptr
//         - cur 向右移动：cur = cur->right
//   
//   3. 当 cur == nullptr 时，遍历结束
//
// 【关键性质】
//   - 有左子树的节点会被访问 2 次
//   - 没有左子树的节点只会被访问 1 次
//   - 时间复杂度 O(N)，空间复杂度 O(1)
//
// 【如何实现前序、中序、后序】
//   - 前序：第一次到达节点时处理（或无左子树时处理）
//   - 中序：第二次到达节点时处理（或无左子树时处理）
//   - 后序：第二次到达节点时，逆序处理左子树的右边界
//
// ============================================================================

namespace morris {

    // 二叉树节点定义
    struct TreeNode {
        int val;
        TreeNode* left;
        TreeNode* right;
        
        TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    };

    // ========================================================================
    // Morris 遍历基础版本
    // ========================================================================
    // 遍历整棵树，对每个节点调用 visit 函数
    // visit 的第二个参数表示是否是第一次访问该节点
    //   - true: 第一次访问（或该节点无左子树）
    //   - false: 第二次访问
    // ========================================================================
    void morris(TreeNode* head, std::function<void(TreeNode*, bool)> visit);

    // ========================================================================
    // 基于 Morris 遍历的前序遍历
    // ========================================================================
    // 前序遍历顺序：根 -> 左 -> 右
    // 策略：在第一次到达节点时处理
    // ========================================================================
    std::vector<int> preorderMorris(TreeNode* head);

    // ========================================================================
    // 基于 Morris 遍历的中序遍历
    // ========================================================================
    // 中序遍历顺序：左 -> 根 -> 右
    // 策略：在第二次到达节点时处理（或无左子树时处理）
    // ========================================================================
    std::vector<int> inorderMorris(TreeNode* head);

    // ========================================================================
    // 基于 Morris 遍历的后序遍历
    // ========================================================================
    // 后序遍历顺序：左 -> 右 -> 根
    // 策略：在第二次到达节点时，逆序处理该节点左子树的右边界
    //       最后再逆序处理整棵树的右边界
    // ========================================================================
    std::vector<int> postorderMorris(TreeNode* head);

    // ========================================================================
    // 辅助函数：构建测试用的二叉树
    // ========================================================================
    // 从数组构建二叉树，-1 表示空节点（层序遍历格式）
    // 例如：[1, 2, 3, -1, 4] 表示：
    //       1
    //      / \
    //     2   3
    //      \
    //       4
    // ========================================================================
    TreeNode* buildTree(const std::vector<int>& arr);

    // 释放二叉树内存
    void freeTree(TreeNode* root);

}
