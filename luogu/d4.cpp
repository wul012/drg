#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ============================================================
// 题目：石子合并 (Lanqiao OJ 1233)
// ============================================================
//
// 【题目描述】
// N 堆石子排成一排，第 i 堆有 a[i] 颗。
// 每次只能合并**相邻**的两堆，代价 = 合并后这堆的石子总数。
// 不断合并直到只剩一堆。求最小总代价。
//
// 【为什么不能用贪心？】
// "每次挑最小的两堆合并"（霍夫曼树）适用于任意两堆都能合并的情况。
// 但本题限制了"只能合并相邻的两堆"，所以贪心不成立。
// 例如 [1, 100, 1]：贪心选 1+1=2 → 但 1 和 1 不相邻，不合法。
//
// 【区间 DP】
// 定义 dp[i][j] = 将区间 [i, j] 的石子全部合并成一堆的最小总代价。
//
// 状态转移：
//   最后一步一定是把两个子堆合成一堆。
//   假设最后一刀切在位置 k，左半 [i, k] 已经合并好了，右半 [k+1, j] 也合并好了，
//   最后把这两堆合一，代价 = 左半代价 + 右半代价 + (整个区间的石子总数)。
//   dp[i][j] = min{ dp[i][k] + dp[k+1][j] } + (sum[j] - sum[i-1])
//              k 从 i 到 j-1
//
// 为什么最后一步的代价是 sum[j]-sum[i-1]？
//   因为合并两堆石子的代价 = 合并后新堆的石子数 = 区间内所有石子之和。
//
// 【枚举顺序：为什么外层是区间长度？】
// 计算 dp[i][j]（长度 len=j-i+1 的区间）需要用到更短的区间 dp[i][k] 和 dp[k+1][j]。
// 所以必须先算短区间，再算长区间 → 外层循环从 len=2 到 len=n。
//
// 基础情况：dp[i][i] = 0（单堆不需要合并）。
//
// 【时间复杂度】O(N³)，空间 O(N²)。N <= 300 时约 2.7×10⁷，可以通过。
// ============================================================

const int INF = 1e9;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    if (!(cin >> n)) return 0;

    vector<int> a(n + 1, 0);
    // 前缀和：O(1) 算区间石子总数 sum[j] - sum[i-1]
    vector<int> sum(n + 1, 0);

    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
        sum[i] = sum[i - 1] + a[i];
    }

    // dp[i][j] = 合并 [i,j] 的最小总代价。dp[i][i] = 0（初始值自动为 0）。
    vector<vector<int>> dp(n + 1, vector<int>(n + 1, 0));

    // ============================================================
    // 区间 DP 三重循环
    // ============================================================
    // 外层：枚举区间长度 len（从 2 开始，长度 1 的区间代价为 0）
    for (int len = 2; len <= n; ++len) {
        // 中层：枚举左端点 i，右端点 j = i + len - 1
        for (int i = 1; i <= n - len + 1; ++i) {
            int j = i + len - 1;
            
            dp[i][j] = INF;
            
            // 内层：枚举断点 k，把 [i,j] 分成 [i,k] 和 [k+1,j]
            for (int k = i; k < j; ++k) {
                int cost = dp[i][k] + dp[k + 1][j] + (sum[j] - sum[i - 1]);
                dp[i][j] = min(dp[i][j], cost);
            }
        }
    }

    cout << dp[1][n] << "\n";

    return 0;
}

