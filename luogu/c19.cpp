#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ============================================================
// 题目：完全背包（经典动态规划）
// ============================================================
//
// 【题目要求】
// 有 n 种物品，第 i 种物品的体积为 w[i]，价值为 v[i]，每种物品可以取无限次。
// 背包容量为 m，求在不超过容量的前提下，能获得的最大总价值。
//
// 【输入格式】
// 第一行：n m
// 接下来 n 行：每行两个整数 w v
//
// 【输出格式】
// 输出一个整数，表示最大总价值。
//
// 【样例输入】
// 3 5
// 1 2
// 2 4
// 3 4
//
// 【样例输出】
// 10
//
// 【样例解释】
// 选 5 个体积为 1、价值为 2 的物品，总价值 10。
//
// 【和 01 背包有什么区别？】
// 01 背包：每件物品只能选 0 次或 1 次。
// 完全背包：每种物品可以选无限次。
//
// 最大区别体现在“枚举容量的顺序”上：
// - 01 背包：体积 j 要倒序枚举
// - 完全背包：体积 j 要正序枚举
//
// 为什么完全背包要正序？
// 因为当我们更新 dp[j] 时，想允许“同一种物品重复使用”，
// 那么 dp[j - w] 应该已经包含“本轮这个物品用过若干次”的结果。
// 这只有在正序枚举时才能做到。
//
// 【状态定义】
// dp[j] = 容量恰好不超过 j 时能得到的最大价值
//
// 【转移方程】
// dp[j] = max(dp[j], dp[j - w[i]] + v[i])
// 其中 j 从 w[i] 到 m 正序枚举
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    if (!(cin >> n >> m)) return 0;

    vector<int> dp(m + 1, 0);

    for (int i = 1; i <= n; ++i) {
        int w, v;
        cin >> w >> v;

        for (int j = w; j <= m; ++j) {
            dp[j] = max(dp[j], dp[j - w] + v);
        }
    }

    cout << dp[m] << '\n';
    return 0;
}

/*
============================================================
【手推样例】
物品：
1) 体积 1，价值 2
2) 体积 2，价值 4
3) 体积 3，价值 4
容量 m=5

初始 dp 全为 0

先处理物品 1（1,2），并正序枚举 j=1~5：
- dp[1] = max(0, dp[0]+2) = 2
- dp[2] = max(0, dp[1]+2) = 4
- dp[3] = 6
- dp[4] = 8
- dp[5] = 10

注意这里 dp[2] 用到了刚更新过的 dp[1]，
这就等价于“同一种物品可以继续再拿一次”。
这正是完全背包的核心。

再处理其他物品后，答案仍然是 10。
============================================================
【易错点总结】
1. 完全背包一定是 j 从小到大正序枚举。
2. 01 背包与完全背包最容易混淆的就是这一点。
3. 一维优化时，dp[j] 表示当前已经考虑过前若干种物品后的最优值。
4. 如果题目要求“恰好装满”，初始化方式会不同，本题是不超过容量即可。
============================================================
*/
