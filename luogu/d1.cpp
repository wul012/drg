#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ============================================================
// 题目：数字三角形 (Lanqiao OJ 1536)
// ============================================================
//
// 【题目描述】
// 给出一个 N 层的数字三角形，从顶部出发，每一步只能向左下或右下走一格。
// 求从顶到底的路径上，数字之和的最大值。
//
// 例如 N=4 的三角形：
//       7
//      3 8
//     8 1 0
//    2 7 4 4
//
// 最优路径：7 → 3 → 8 → 7 = 25  或  7 → 8 → 1 → 7 = 23
// 实际最优：7 → 3 → 8 → 7 = 25
//
// 【DP 思路：自顶向下 vs 自底向上】
// 方法 1（自顶向下）：
//   dp[i][j] = 从顶部走到 (i,j) 的最大路径和
//   dp[i][j] = dp[i][j] + max(dp[i-1][j-1], dp[i-1][j])
//   问题：需要特判边界（j=1 时没有左上，j=i 时没有正上），
//         而且最终答案要在最底层遍历一遍 max(dp[n][1..n])。
//
// 方法 2（自底向上，本代码采用）：
//   dp[i][j] = 从 (i,j) 出发走到底的最大路径和
//   dp[i][j] = 原始值 + max(dp[i+1][j], dp[i+1][j+1])
//   优点：不需要边界特判（dp[n+1][...] 初始化为 0 即可），
//         答案直接就是 dp[1][1]，一步到位！
//
// 【为什么自底向上更简洁？】
// 第 i 层第 j 个元素的"左下"是 (i+1, j)，"右下"是 (i+1, j+1)。
// 两个子问题都一定存在（只要 i < n），不需要特殊处理边界。
// 而自顶向下时，(i, j) 的"左上"是 (i-1, j-1)，当 j=1 时就越界了。
//
// 【时间复杂度】O(N²)，空间 O(N²)（可优化为 O(N)，但这里为了清晰不做）
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    if (!(cin >> n)) return 0;

    // dp 数组开 (n+2)×(n+2)，多出来的部分初始化为 0
    // 这样 dp[n+1][j] = 0，作为"三角形底部下方的虚拟层"，省去边界判断
    vector<vector<int>> dp(n + 2, vector<int>(n + 2, 0));

    // 读入三角形（第 i 层有 i 个数）
    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= i; ++j) {
            cin >> dp[i][j];
        }
    }

    // ============================================================
    // 自底向上 DP
    // ============================================================
    // 从第 n-1 层开始（第 n 层是底层，不需要更新，它本身就是"从这里到底"的值）
    for (int i = n - 1; i >= 1; --i) {
        for (int j = 1; j <= i; ++j) {
            // dp[i+1][j]   = 左下方格子的最大路径和
            // dp[i+1][j+1] = 右下方格子的最大路径和
            // 取较大的那个加上当前格子的值
            dp[i][j] += max(dp[i + 1][j], dp[i + 1][j + 1]);
        }
    }

    // 所有路径的最大值自底向顶汇聚到 dp[1][1]
    cout << dp[1][1] << "\n";

    return 0;
}

