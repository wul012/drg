#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ============================================================
// 题目：背包与魔法 (Lanqiao OJ 2223)
// ============================================================
//
// 【题目描述】
// N 个物品，第 i 个物品重量 w_i、价值 v_i。背包容量为 M。
// 你有 1 次"魔法"机会：可以选择某一件【已放入背包的】物品，
// 把它的重量变成 w_i + K，同时价值翻倍变成 2 * v_i。
// 魔法也可以不用。求背包装下的最大总价值。
//
// 【核心思路：01 背包 + 额外维度记录魔法状态】
// 普通 01 背包只有一维状态 dp[j] = "容量为 j 时的最大价值"。
// 现在多了一个"是否已用魔法"的决策，所以需要多加一维：
//   dp[j][0] = 容量 j、从未使用魔法时 的最大价值
//   dp[j][1] = 容量 j、已使用过 1 次魔法时 的最大价值
//
// 【为什么要逆序遍历 j？】
// 和普通 01 背包一样，一维滚动数组必须逆序遍历容量 j，
// 保证当前物品最多只被选一次（正序会导致同一物品被重复选取，变成完全背包）。
//
// 【三种决策】
// 对于每件物品 (w, v)，在容量 j 处有 3 种选择：
//   (1) 不选这件物品 → dp[j][0], dp[j][1] 保持不变
//   (2) 选这件物品，不对它施魔法 → 需要 w 容量，获得 v 价值
//       dp[j][0] = max(dp[j][0], dp[j-w][0] + v)      — 之前没用魔法，现在也不用
//       dp[j][1] = max(dp[j][1], dp[j-w][1] + v)      — 之前已用魔法（用在别的物品上了），现在正常拿
//   (3) 选这件物品，且对它施魔法 → 需要 w+K 容量，获得 2v 价值
//       dp[j][1] = max(dp[j][1], dp[j-(w+K)][0] + 2v) — 必须从"之前没用魔法"的状态转移过来
//       （不能从 dp[...][1] 转移，因为那样魔法已经用过了，不能再用第二次）
//
// 【转移顺序的关键细节】
// 在同一个 j 上，"选项 2" 和 "选项 3" 谁先算？
// 由于都是在读 dp[j-...] 的旧值（逆序保证了 j-w < j 还没被更新），
// 所以两个选项的顺序不影响正确性。但必须把"选项 2 更新 dp[j][0]"放在
// "选项 3 读 dp[j-(w+K)][0]"之前吗？不用担心——因为逆序遍历下
// j-(w+K) < j，还没被本层循环更新过，仍是上一层（上一个物品）的值。
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m, k_magic;
    if (!(cin >> n >> m >> k_magic)) return 0;

    // dp[j][0]: 容量 j、未用魔法的最大价值
    // dp[j][1]: 容量 j、已用魔法的最大价值
    vector<vector<long long>> dp(m + 1, vector<long long>(2, 0));

    for (int i = 0; i < n; ++i) {
        int w, v;
        cin >> w >> v;

        // 01 背包必须逆序遍历容量，防止同一物品被选多次
        for (int j = m; j >= 0; --j) {
            // 决策 1：不选 → dp[j][0], dp[j][1] 自动保持旧值，无需代码

            // 决策 2：选，不施魔法（消耗容量 w，获得价值 v）
            if (j >= w) {
                dp[j][0] = max(dp[j][0], dp[j - w][0] + v);
                // 如果之前已经在别的物品上用过魔法了，也可以正常拿这件
                dp[j][1] = max(dp[j][1], dp[j - w][1] + v);
            }

            // 决策 3：选，且对这件物品施魔法（消耗容量 w+K，获得价值 2v）
            // 前提：之前从未使用过魔法（所以从 dp[...][0] 转移到 dp[...][1]）
            if (j >= w + k_magic) {
                dp[j][1] = max(dp[j][1], dp[j - (w + k_magic)][0] + 2LL * v);
            }
        }
    }

    // 答案：取用魔法和不用魔法两种最终状态中的较大者
    cout << max(dp[m][0], dp[m][1]) << "\n";

    return 0;
}

