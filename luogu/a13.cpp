#include<bits/stdc++.h>
using namespace std;
const int N = 1e5+9;
int a[N];

int main() {
    // 优化输入输出流
    ios::sync_with_stdio(0),cin.tie(0),cout.tie(0);

    // m 代表每组（如每艘船）的最大容量
    // n 代表物品（如乘客）的总数
    int m,n;cin>>m>>n;
    
    // 读入每个物品的重量
    for (int i = 1 ; i<=n;++i)cin>>a[i];
    
    // 对物品重量进行从小到大排序
    sort(a+1,a+1+n);
    
    // l 指向最轻的物品，r 指向最重的物品
    // ans 记录需要的总组数（船数）
    int l=1,r=n,ans=0;
    
    // 双指针贪心匹配
    while (l<=r) {
        ans++; // 无论怎样，都需要分配新的一组（船）
        
        if (l==r)break; // 如果只剩最后一个物品，分完这组直接结束
        
        // 贪心策略：尝试把当前最轻(a[l])和最重(a[r])的组合在一起
        // 如果它们加起来不超过容量 m，说明可以同乘一组（船）
        if (a[l]+a[r]<=m){
            l++; // 最轻的被安排了，左指针右移
            r--; // 最重的被安排了，右指针左移
        } else {
            // 如果加起来超重，说明最重的那个物品只能自己单独占一组（船）
            // 因为它和最轻的都无法组合，肯定无法和其他任何物品组合
            r--; // 最重的自己走，右指针左移
        }
    }
    
    // 输出最小的分组数
    cout<<ans<<'\n';
    return 0;
}