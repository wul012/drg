#include <iostream>
#include <vector>

using namespace std;
using ll = long long;

// ============================================================
// 题目：N 皇后计数（DFS + 回溯 + 剪枝）
// ============================================================
//
// 【题目要求】
// 在 n×n 棋盘上放 n 个皇后，使得任意两个皇后都不能互相攻击。
// 皇后的攻击范围包括：
// - 同一行
// - 同一列
// - 同一条主对角线
// - 同一条副对角线
//
// 求总方案数。
//
// 【输入格式】
// 一行一个整数 n
//
// 【输出格式】
// 输出方案总数。
//
// 【样例输入】
// 4
//
// 【样例输出】
// 2
//
// 【为什么用 DFS / 回溯？】
// 因为每一行最终只能放一个皇后，所以可以按“行”来搜索。
// 在第 row 行枚举列 col，看当前位置能不能放。
// 如果能放，就做标记，递归搜索下一行；搜索完再撤销标记。
// 这就是经典的回溯。
//
// 【三个判冲突数组】
// 1. col[c]     : 第 c 列是否已有皇后
// 2. diag1[idx] : 主对角线是否已有皇后
// 3. diag2[idx] : 副对角线是否已有皇后
//
// 对角线编号规律：
// - 主对角线：row - col 相同
// - 副对角线：row + col 相同
//
// 由于 row-col 可能为负，所以通常给主对角线整体平移一个 +n：
// diag1[row - col + n]
// diag2[row + col]
//
// 【为什么这样编号不会冲突？】
// 因为同一条主对角线上的格子，它们的 row-col 恒定不变；
// 同一条副对角线上的格子，它们的 row+col 恒定不变。
// 这正好把“几何攻击关系”转换成了“数组下标是否已占用”。
// ============================================================

int n;
ll ans = 0;
vector<bool> colUsed, diag1Used, diag2Used;

void dfs(int row) {
    if (row > n) {
        ans++;
        return;
    }

    for (int col = 1; col <= n; ++col) {
        int d1 = row - col + n;
        int d2 = row + col;

        if (colUsed[col] || diag1Used[d1] || diag2Used[d2]) continue;

        colUsed[col] = true;
        diag1Used[d1] = true;
        diag2Used[d2] = true;

        dfs(row + 1);

        // 回溯：撤销本层决策，恢复现场
        colUsed[col] = false;
        diag1Used[d1] = false;
        diag2Used[d2] = false;
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    if (!(cin >> n)) return 0;

    colUsed.assign(n + 1, false);
    diag1Used.assign(2 * n + 5, false);
    diag2Used.assign(2 * n + 5, false);

    dfs(1);
    cout << ans << '\n';
    return 0;
}

