#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// 题目：小明的背包1 (蓝桥杯 01背包问题)
// 题意：给定 N 个物品，每个物品有一个体积 w 和一个价值 v。背包的最大容量为 V。
// 求在不超过背包容量的情况下，能够装入的最大价值。
// 这是最经典的 01 背包问题（每个物品只能用 1 次或者 0 次）。

// 数据范围：
// N <= 100
// V <= 1000
// w_i, v_i <= 1000
// 因此 O(N * V) = 10^5 的时间复杂度完全可以接受。

int main() {
    // 优化输入输出流
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int N, V;
    if (!(cin >> N >> V)) return 0;

    // dp[j] 表示背包容量刚好为 j 时，能够获得的最大价值。
    // 这里使用了一维数组滚动优化的技巧。
    // 原本的 01 背包状态转移方程是二维的：
    // dp[i][j] = max(dp[i-1][j], dp[i-1][j - w[i]] + v[i])
    // 因为第 i 层的状态仅仅依赖于第 i-1 层，所以可以把第一维（物品数量维）给压缩掉。
    // 但是压缩后，为了保证每个物品只被放入 1 次，我们在遍历体积 j 时，必须【逆序遍历】！
    vector<int> dp(V + 1, 0);

    for (int i = 0; i < N; ++i) {
        int w, v;
        cin >> w >> v;

        // 【核心优化：一维数组的逆序遍历】
        // 为什么要逆序 (j 从 V 递减到 w)？
        // 假设我们正序遍历（j 从 w 递增到 V），当我们在计算 dp[j] = dp[j - w] + v 时，
        // 此时的 dp[j - w] 已经在这一层被更新过了（也就是可能已经包含了当前这个物品 i）！
        // 这样就变成了“完全背包问题”（一个物品可以被拿多次）。
        // 
        // 为了确保我们用到的 dp[j - w] 是属于“上一层”（即物品 i 还没放进去的时候）的干净状态，
        // 我们必须从后往前遍历。因为当你算 dp[j] 时，比 j 小的 dp[j - w] 还没被这一层的循环碰过，
        // 它还是原汁原味的“上一层”的旧数据。这样就保证了每个物品最多只被拿一次。
        for (int j = V; j >= w; --j) {
            // 状态转移：选或者不选当前这件物品，取最大值
            // 不选：还是原来的 dp[j]
            // 选：拿走 w 的体积，换来 v 的价值，即 dp[j - w] + v
            dp[j] = max(dp[j], dp[j - w] + v);
        }
    }

    // 最终 dp[V] 就是体积不超过 V 时能达到的最大价值
    cout << dp[V] << "\n";

    return 0;
}
