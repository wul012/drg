#include <iostream>

using namespace std;
using ll = long long;
using i128 = __int128_t;

// ============================================================
// 题目：快速幂取模（二进制快速幂）
// ============================================================
//
// 【题目要求】
// 给定三个整数 a, b, p，求 a^b mod p 的值。
//
// 【输入格式】
// 一行三个整数 a b p
//
// 【输出格式】
// 输出一个整数，表示 a^b mod p
//
// 【样例输入】
// 2 10 1000
//
// 【样例输出】
// 24
//
// 【样例解释】
// 2^10 = 1024，1024 mod 1000 = 24。
//
// 【为什么不能直接循环乘 b 次？】
// 朴素做法要做 b 次乘法，复杂度是 O(b)。
// 当 b 很大时会非常慢。
//
// 【快速幂核心思想】
// 利用指数的二进制展开。
//
// 例如：
// 13 的二进制是 1101
// 所以：
// a^13 = a^(8+4+1) = a^8 * a^4 * a^1
//
// 我们不断把底数平方：
// a, a^2, a^4, a^8, ...
// 每看指数 b 的一位：
// - 如果当前位是 1，就把当前底数乘进答案
// - 然后底数自乘，指数右移一位
//
// 这样总复杂度就从 O(b) 降到 O(log b)。
//
// 【为什么要边乘边取模？】
// 因为乘法结果会非常大。
// 根据同余性质：
// (x * y) mod p = ((x mod p) * (y mod p)) mod p
// 所以完全可以每一步都先取模，避免数值爆炸。
//
// 【实现细节】
// 如果 a 和 p 较大，a*a 可能超出 long long。
// 所以这里乘法先转成 __int128，再取模，更稳妥。
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll a, b, p;
    if (!(cin >> a >> b >> p)) return 0;

    ll ans = 1 % p;
    a %= p;

    while (b > 0) {
        if (b & 1) {
            ans = (ll)((i128)ans * a % p);
        }
        a = (ll)((i128)a * a % p);
        b >>= 1;
    }

    cout << ans << '\n';
    return 0;
}

/*
============================================================
【手推样例】
a=2, b=10, p=1000

10 的二进制是 1010

初始：ans=1, a=2

b=10（二进制最低位 0）
- 不乘入答案
- a 变成 4
- b 右移变 5

b=5（最低位 1）
- ans = 1 * 4 = 4
- a 变成 16
- b 右移变 2

b=2（最低位 0）
- 不乘入答案
- a 变成 256
- b 右移变 1

b=1（最低位 1）
- ans = 4 * 256 = 1024 mod 1000 = 24
- a 再平方已无所谓
- b 右移变 0，结束

答案 = 24
============================================================
【易错点总结】
1. 看到求 a^b mod p，第一反应应是快速幂。
2. 要先做 a%=p，过程中也要不断取模。
3. 判断当前二进制位是否为 1，用的是 b & 1。
4. 指数每轮右移一位，底数每轮平方一次。
============================================================
*/
