#include <iostream>
#include <vector>
#include <deque>

using namespace std;

// ============================================================
// 题目：滑动窗口最值（单调队列）
// ============================================================
//
// 【题目要求】
// 给定一个长度为 n 的数组和窗口大小 k。
// 窗口从左到右滑动，每次向右移动一格。
// 对每个长度为 k 的窗口，输出其中的最小值和最大值。
//
// 【输入格式】
// 第一行：n k
// 第二行：n 个整数
//
// 【输出格式】
// 第一行输出每个窗口的最小值
// 第二行输出每个窗口的最大值
//
// 【样例输入】
// 8 3
// 1 3 -1 -3 5 3 6 7
//
// 【样例输出】
// -1 -3 -3 -3 3 3
// 3 3 5 5 6 7
//
// 【为什么暴力不行？】
// 如果每个窗口都重新扫描 k 个数找最值，复杂度是 O(nk)。
// 当 n、k 都很大时会超时。
//
// 【单调队列核心思想】
// 队列里存“下标”，不是直接存值。
// 这样可以同时做到：
// 1. 判断某个元素是否已经滑出窗口；
// 2. 比较 a[下标] 的大小关系。
//
// 对“最小值队列”：
// - 队列中的值要保持单调递增
// - 队首永远是当前窗口的最小值下标
//
// 对“最大值队列”：
// - 队列中的值要保持单调递减
// - 队首永远是当前窗口的最大值下标
//
// 【最小值队列的操作规则】
// 当处理到下标 i：
// 1. 如果队首已经不在窗口范围内，就弹出队首
// 2. 为了保持递增，把队尾所有 >= a[i] 的下标弹掉
// 3. 把 i 压入队尾
// 4. 当 i >= k 时，队首就是当前窗口最小值
//
// 最大值同理，只要把大小关系反过来即可。
//
// 【时间复杂度】
// 每个元素最多入队一次、出队一次，总复杂度 O(n)
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, k;
    if (!(cin >> n >> k)) return 0;

    vector<int> a(n + 1);
    for (int i = 1; i <= n; ++i) cin >> a[i];

    deque<int> qmin, qmax;
    vector<int> mins, maxs;

    for (int i = 1; i <= n; ++i) {
        // 维护最小值队列：队首必须仍在窗口内
        while (!qmin.empty() && qmin.front() < i - k + 1) qmin.pop_front();
        // 保持队列中的值单调递增
        while (!qmin.empty() && a[qmin.back()] >= a[i]) qmin.pop_back();
        qmin.push_back(i);

        // 维护最大值队列：队首必须仍在窗口内
        while (!qmax.empty() && qmax.front() < i - k + 1) qmax.pop_front();
        // 保持队列中的值单调递减
        while (!qmax.empty() && a[qmax.back()] <= a[i]) qmax.pop_back();
        qmax.push_back(i);

        if (i >= k) {
            mins.push_back(a[qmin.front()]);
            maxs.push_back(a[qmax.front()]);
        }
    }

    for (int i = 0; i < (int)mins.size(); ++i) {
        cout << mins[i] << (i + 1 == (int)mins.size() ? '\n' : ' ');
    }
    for (int i = 0; i < (int)maxs.size(); ++i) {
        cout << maxs[i] << (i + 1 == (int)maxs.size() ? '\n' : ' ');
    }

    return 0;
}

/*
============================================================
【手推样例】
数组：1 3 -1 -3 5 3 6 7，k=3

窗口 1：[1,3,-1]
最小值 = -1，最大值 = 3

窗口 2：[3,-1,-3]
最小值 = -3，最大值 = 3

窗口 3：[-1,-3,5]
最小值 = -3，最大值 = 5

继续滑动，最终得到：
最小值：-1 -3 -3 -3 3 3
最大值：3 3 5 5 6 7

拿“最小值队列”举例：
处理到 -1 时，前面的 3 会被弹掉，因为它比 -1 大，
而且 3 比 -1 更靠左，以后也不可能成为更优的最小值候选。
这就是单调队列能优化到 O(n) 的原因。
============================================================
【易错点总结】
1. 队列里存的是下标，不是数值。
2. 先弹出窗口外元素，再维护单调性。
3. 最小值队列弹队尾条件是 >=，最大值队列是 <=。
4. 只有当 i >= k 时，才形成了一个完整窗口，才能输出答案。
============================================================
*/
