#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ============================================================
// 题目：蓝桥公园 (Lanqiao OJ 1121)
// ============================================================
//
// 【题目描述】
// 给定 N 个景点和 M 条道路（双向边，可能有重边）。
// Q 次查询，每次给出 st 和 ed，输出 st 到 ed 的最短路径长度。不连通输出 -1。
//
// 【为什么选 Floyd？】
// - 需要处理"多次查询、任意两点之间"的最短路 → 多源最短路问题。
// - N <= 400，Floyd 的 O(N³) ≈ 6.4×10⁷，1 秒内完全能跑。
// - 如果 N 很大（比如 10⁵），Floyd 就不能用了，得用 Dijkstra 跑 Q 次或其他方法。
//
// 【Floyd-Warshall 算法原理】
// 本质是动态规划，定义三维状态：
//   dp[k][i][j] = "只允许使用编号 1~k 的节点作为中转站时，i 到 j 的最短距离"
//
// 状态转移：
//   dp[k][i][j] = min(dp[k-1][i][j],           ← 不经过节点 k
//                     dp[k-1][i][k] + dp[k-1][k][j])  ← 经过节点 k
//
// 因为 dp[k] 只依赖 dp[k-1]，所以可以滚动压缩掉第一维，
// 变成在二维数组 dp[i][j] 上原地更新。
// 关键：k 必须在最外层循环！如果把 k 放在内层，计算顺序就错了。
//
// 【INF 的选取】
// 边权 w <= 10⁹，路径最多经过 400 个点，最大距离 ≈ 4×10¹¹，远超 int 范围。
// 所以用 long long 存距离，INF 设为 10¹⁸（两个 INF 相加仍不超过 long long 上限 9.2×10¹⁸）。
// 不能用常见的 0x3f3f3f3f（约 10⁹），两个加起来就溢出 int 了。
//
// 【重边处理】
// 两点间可能有多条边，只保留最短的那一条（读入时取 min）。
// ============================================================

const long long INF = 1e18;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m, q;
    if (!(cin >> n >> m >> q)) return 0;

    // dp[i][j] = 从点 i 到点 j 的当前已知最短距离
    vector<vector<long long>> dp(n + 1, vector<long long>(n + 1, INF));

    // 自己到自己距离为 0
    for (int i = 1; i <= n; ++i) {
        dp[i][i] = 0;
    }

    // 读入 M 条双向道路，遇到重边取 min
    for (int i = 0; i < m; ++i) {
        int u, v;
        long long w;
        cin >> u >> v >> w;
        dp[u][v] = min(dp[u][v], w);
        dp[v][u] = min(dp[v][u], w);
    }

    // ============================================================
    // 核心：Floyd 三重循环（k 在最外层！）
    // ============================================================
    // 每轮循环"解锁"节点 k 作为中转站。
    // 对所有点对 (i, j)，尝试走 i→k→j 看看是否比当前路更短。
    for (int k = 1; k <= n; ++k) {
        for (int i = 1; i <= n; ++i) {
            // 小优化：如果 i 根本到不了 k，那 i→k→j 对任何 j 都没用，跳过
            if (dp[i][k] == INF) continue;
            for (int j = 1; j <= n; ++j) {
                if (dp[k][j] != INF) {
                    dp[i][j] = min(dp[i][j], dp[i][k] + dp[k][j]);
                }
            }
        }
    }

    // 处理 Q 次查询
    while (q--) {
        int st, ed;
        cin >> st >> ed;
        if (dp[st][ed] == INF) {
            cout << -1 << "\n";
        } else {
            cout << dp[st][ed] << "\n";
        }
    }

    return 0;
}

