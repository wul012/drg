#include <iostream>
#include <vector>
#include <string>
#include <algorithm>

using namespace std;

// ============================================================
// 题目：编辑距离（经典字符串 DP）
// ============================================================
// ============================================================
//
// 【题目要求】
// 给定两个字符串 s 和 t。
// 你可以对字符串 s 执行以下三种操作：
// 1. 插入一个字符
// 2. 删除一个字符
// 3. 替换一个字符
//
// 求把 s 变成 t 的最少操作次数。
//
// 【输入格式】
// 第一行：字符串 s
// 第二行：字符串 t
//
// 【输出格式】
// 输出一个整数，表示最少操作次数。
//
// 【样例输入】
// horse
// ros
//
// 【样例输出】
// 3
//
// 【样例解释】
// 一种可行方案：
// horse -> rorse （把 h 替换成 r）
// rorse -> rose  （删除一个 r）
// rose  -> ros   （删除 e）
// 共 3 步。
//
// 【为什么想到 DP？】
// 和 LCS 一样，本题也可以按“前缀”来拆分问题。
// 设想：
// 要把 s 的前 i 个字符变成 t 的前 j 个字符，
// 最后一步操作是什么？
//
// - 可能是删除 s[i-1]
// - 可能是在 s 末尾插入 t[j-1]
// - 可能是把 s[i-1] 替换成 t[j-1]
// - 如果本来就相同，也可能什么都不用做
//
// 这就自然形成了状态转移。
//
// 【状态定义】
// dp[i][j] = 把 s 的前 i 个字符变成 t 的前 j 个字符的最少操作数
//
// 【边界】
// dp[i][0] = i
// 表示把长度为 i 的字符串变成空串，只能删 i 次
//
// dp[0][j] = j
// 表示把空串变成长度为 j 的字符串，只能插入 j 次
//
// 【转移】
// 若 s[i-1] == t[j-1]：
// dp[i][j] = dp[i-1][j-1]
// 因为最后一个字符已经相同，不需要额外操作
//
// 若 s[i-1] != t[j-1]：
// 1. 删除：dp[i-1][j] + 1
// 2. 插入：dp[i][j-1] + 1
// 3. 替换：dp[i-1][j-1] + 1
// 取三者最小
//
// 【时间复杂度】
// O(nm)
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    string s, t;
    if (!(cin >> s >> t)) return 0;

    int n = (int)s.size();
    int m = (int)t.size();
    vector<vector<int>> dp(n + 1, vector<int>(m + 1, 0));

    for (int i = 0; i <= n; ++i) dp[i][0] = i;
    for (int j = 0; j <= m; ++j) dp[0][j] = j;

    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            if (s[i - 1] == t[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1];
            } else {
                dp[i][j] = min({
                    dp[i - 1][j] + 1,
                    dp[i][j - 1] + 1,
                    dp[i - 1][j - 1] + 1
                });
            }
        }
    }

    cout << dp[n][m] << '\n';
    return 0;
}

/*
============================================================
【手推样例】
s = "horse"
t = "ros"

一个可行变换：
1. horse -> rorse   替换 h 为 r
2. rorse -> rose    删除一个 r
3. rose  -> ros     删除 e
所以答案不超过 3。

DP 会系统比较所有可能的“最后一步操作”，
最终算出最优值正好也是 3。

如果最后一个字符相同，比如都已经处理到 's'，
那就直接继承左上角 dp[i-1][j-1]；
如果不同，就考虑删、插、替三种操作中的最优方案。
============================================================
【易错点总结】
1. 删除、插入、替换三种操作都要考虑。
2. 空串边界初始化非常关键：dp[i][0]=i, dp[0][j]=j。
3. 字符相同不是 +1，而是直接继承 dp[i-1][j-1]。
4. 本题与 LCS 很像，但转移逻辑不同，不要混淆。
============================================================
*/
