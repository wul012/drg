#include<bits/stdc++.h>
using namespace std;

// 题目：求满足某种规则的数列个数（半数分拆问题）
// 规则：对于一个数 n，可以在其左边加上一个不超过 n/2 的自然数，然后继续这个过程，直到不能加为止。
// N 取 20 足够了，因为 20 连续除以 2 最多经过 5 步就会变成 0
const int N=20;
int a[N];

// dfs: 计算以当前层（第 dep 层）数字为结尾，能够生成多少种数列
// dep 表示当前递归深度（当前数列的长度）
int dfs(int dep) {
    // res = 1：把自己本身算作一种情况（什么都不加，就当前这个数单独作为一种数列方案）
    int res=1;
    
    // 枚举可以在当前数字左边添加的所有可能的数字 i
    // 题目规则：添加的数字不能超过当前数字（也就是前一层选定的数字 a[dep-1]）的一半
    for (int i = 1;i<=a[dep-1]/2;++i) {
        a[dep]=i; // 在第 dep 层选择数字 i
        
        // 递归进入下一层（dep+1），并将下一层返回的方案数累加到结果中
        res+=dfs(dep+1);
    }
    
    // 返回总的方案数
    return res;
}

int main() {
    int n;cin>>n;
    
    // 将初始数字 n 放在数列的第一层（dep=1）
    a[1]=n;
    
    // 从第二层开始递归，计算方案数
    cout<<dfs(2)<<'\n';

    return 0;
}