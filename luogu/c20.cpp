#include <iostream>
#include <vector>
#include <string>
#include <algorithm>

using namespace std;

// ============================================================
// 题目：最长公共子序列 LCS（经典二维 DP）
// ============================================================
//
// 【题目要求】
// 给定两个字符串 s 和 t，求它们的最长公共子序列长度。
// 子序列不要求连续，但必须保持原有相对顺序。
//
// 【输入格式】
// 第一行：字符串 s
// 第二行：字符串 t
//
// 【输出格式】
// 输出一个整数，表示最长公共子序列的长度。
//
// 【样例输入】
// abcde
// ace
//
// 【样例输出】
// 3
//
// 【样例解释】
// 最长公共子序列可以是 "ace"，长度为 3。
//
// 【为什么想到 DP？】
// 当我们比较两个前缀时，最优答案总能由更短前缀的答案转移过来，
// 这就是典型的“前缀型动态规划”。
//
// 【状态定义】
// dp[i][j] = s 的前 i 个字符 与 t 的前 j 个字符 的最长公共子序列长度
//
// 这里“前 i 个字符”指 s[0..i-1]，
// 所以代码里通常开 (n+1)×(m+1) 的数组，方便处理空串边界。
//
// 【转移方程】
// 1. 如果 s[i-1] == t[j-1]
//    说明这两个字符可以配对，
//    dp[i][j] = dp[i-1][j-1] + 1
//
// 2. 如果 s[i-1] != t[j-1]
//    那么这两个位置至少有一个不能同时出现在公共子序列末尾，
//    dp[i][j] = max(dp[i-1][j], dp[i][j-1])
//
// 【为什么不需要看 dp[i-1][j-1]？】
// 因为当字符不同的时候，dp[i-1][j-1] 一定不优于前两者的最大值，
// 它已经被包含在 max(dp[i-1][j], dp[i][j-1]) 的讨论里了。
//
// 【时间复杂度】
// O(nm)
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    string s, t;
    if (!(cin >> s >> t)) return 0;

    int n = (int)s.size();
    int m = (int)t.size();
    vector<vector<int>> dp(n + 1, vector<int>(m + 1, 0));

    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            if (s[i - 1] == t[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1] + 1;
            } else {
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
            }
        }
    }

    cout << dp[n][m] << '\n';
    return 0;
}

/*
============================================================
【手推样例】
s = "abcde"
t = "ace"

我们按前缀来想：
- s 前 1 个字符 "a" 和 t 前 1 个字符 "a"，LCS 长度是 1
- 当比较到 s[2]='c' 与 t[1]='c' 时，它们相同，
  所以可以在之前 LCS 的基础上 +1
- 最后再匹配到 'e'

最终公共子序列可以取：a c e
长度为 3。

如果某一步字符不相等，例如比较 'b' 和 'c'，
那就只能丢掉其中一个字符，
转化成更小前缀的子问题，这就是 max(dp[i-1][j], dp[i][j-1])。
============================================================
【易错点总结】
1. LCS 是“子序列”，不是“子串”，所以不要求连续。
2. dp[i][j] 对应的是前 i 个和前 j 个字符，代码下标要写成 s[i-1]、t[j-1]。
3. 字符相等时用 dp[i-1][j-1]+1。
4. 字符不等时是取上方和左方最大值，不是简单清零。
============================================================
*/
