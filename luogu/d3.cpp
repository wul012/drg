#include <iostream>
#include <vector>

using namespace std;

// ============================================================
// 题目：安全序列 (Lanqiao OJ 3423)
// ============================================================
//
// 【题目描述】
// n 个空位排成一排（编号 1~n），要求放若干个油桶。
// 约束：任意两个油桶之间至少要隔 k 个空位。
// 可以一个都不放。求所有合法放置方案的总数（对 10⁹+7 取模）。
//
// 【朴素 DP → O(N²) 超时】
// 定义 dp[i] = "最后一个油桶放在位置 i" 的合法方案数。
// 如果 i 处放了一个桶，前一个桶只能放在位置 1 ~ i-k-1 中的某一个。
// 所以 dp[i] = dp[1] + dp[2] + ... + dp[i-k-1] + 1
//   （+1 表示"前面一个桶都没放，只在 i 这里放一个"的方案）
// 如果对每个 i 都用循环求和，时间 O(N²)。
//
// 【前缀和优化 → O(N)】
// 令 sum[i] = dp[1] + dp[2] + ... + dp[i]（dp 的前缀和）。
// 则 dp[i] = sum[i-k-1] + 1（当 i > k 时）
//    dp[i] = 1             （当 i <= k 时，前面放不下别的桶）
// sum[i] = sum[i-1] + dp[i]
// 每次 O(1) 完成转移！
//
// 最终答案 = 1（一个都不放）+ dp[1] + dp[2] + ... + dp[n] = sum[n] + 1
//
// 【为什么 dp[i] 要 +1？】
// dp[i] 的含义是"最后一个桶放在 i 处的方案数"。
// 当 i 是整个序列的第一个桶时，前面没有任何桶，这也是一种合法方案。
// 这个"空前缀"的方案数就是那个 +1。
// 你也可以理解为有一个虚拟的 dp[0] = 1。
// ============================================================

const int MOD = 1e9 + 7;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, k;
    if (!(cin >> n >> k)) return 0;

    if (n == 0) {
        cout << 1 << "\n";
        return 0;
    }

    // dp[i] = 最后一个桶放在位置 i 的合法方案数
    vector<long long> dp(n + 1, 0);
    // sum[i] = dp[1] + dp[2] + ... + dp[i]
    vector<long long> sum(n + 1, 0);

    for (int i = 1; i <= n; ++i) {
        if (i <= k) {
            // 前面空间不足 k，只能放 i 这一个桶，方案数 = 1
            dp[i] = 1;
        } else {
            // 前一个桶可放在 1 ~ i-k-1 的任意位置
            // 方案数 = sum[i-k-1]（所有可能的"前一个桶位置"的方案之和）+ 1（只放 i 一个桶）
            dp[i] = (sum[i - k - 1] + 1) % MOD;
        }

        // 维护前缀和
        sum[i] = (sum[i - 1] + dp[i]) % MOD;
    }

    // 总方案 = "一个都不放"(1) + "以各个位置为最后一个桶"(sum[n])
    cout << (sum[n] + 1) % MOD << "\n";

    return 0;
}

/*
============================================================
【手推演示】n=5, k=2

约束：任意两桶间至少隔 2 个空位。

--- 计算 dp 和 sum ---

i=1: i<=k(2) → dp[1]=1
     sum[1]=0+1=1
     含义：只在位置 1 放一个桶 → {1}

i=2: i<=k(2) → dp[2]=1
     sum[2]=1+1=2
     含义：只在位置 2 放一个桶 → {2}

i=3: i>k, dp[3] = sum[3-2-1] + 1 = sum[0] + 1 = 0+1 = 1
     sum[3]=2+1=3
     含义：只在位置 3 放一个桶 → {3}
     （位置 3 前面的 2 个位置都在禁区内，放不了别的桶）

i=4: i>k, dp[4] = sum[4-2-1] + 1 = sum[1] + 1 = 1+1 = 2
     sum[4]=3+2=5
     含义：{4} 或 {1,4}（位置 1 和 4 间隔 2 个空位，合法）

i=5: i>k, dp[5] = sum[5-2-1] + 1 = sum[2] + 1 = 2+1 = 3
     sum[5]=5+3=8
     含义：{5}, {1,5}, {2,5}

--- 汇总 ---
总方案 = sum[5] + 1 = 8 + 1 = 9

枚举验证所有合法方案（两桶间至少隔 2 个空位）：
  0 个桶：{}                                           → 1 种
  1 个桶：{1},{2},{3},{4},{5}                            → 5 种
  2 个桶：{1,4},{1,5},{2,5}                              → 3 种
  3 个桶及以上：5 个位置隔 2 个放不下 3 个桶              → 0 种
  总计 = 1 + 5 + 3 = 9 ✓
============================================================
*/
