#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

// ============================================================
// 题型 8：树状数组 (BIT / Fenwick Tree)
// 【具体真题】：小朋友排队 (蓝桥杯 2014 省赛)
// ============================================================
//
// 【题目描述】
// n 个小朋友排成一排，每个小朋友有一个身高 h[i]。
// 每次只能交换相邻的两个小朋友，最终要让所有小朋友按身高从小到大排好。
// 每个小朋友每参与一次交换，不高兴程度递增：
//   第 1 次被交换 → 不高兴 +1
//   第 2 次被交换 → 不高兴 +2
//   第 k 次被交换 → 不高兴 +k
// 求：所有小朋友的不高兴度之和的最小值。
//
// 【关键数学结论：最少交换次数 = 逆序对数】
// "相邻交换排序" 其实就是冒泡排序。冒泡排序每一次交换恰好消除一个逆序对，
// 所以要把序列排成升序，最少需要的相邻交换次数 = 序列的逆序对总数。
//
// 但题目要求的不是"总交换次数"，而是"每个小朋友各自被交换了几次"。
// 对于小朋友 i，他被交换的次数 =
//   (1) 他前面比他高的人数（这些人最终都要越过他往右走，每越一次就和他交换一次）
// + (2) 他后面比他矮的人数（这些人最终都要越过他往左走，每越一次也和他交换一次）
//
// 设 swap_cnt[i] = 小朋友 i 被交换的总次数
// 则 swap_cnt[i] = 前面比他大的个数 + 后面比他小的个数
// 小朋友 i 的不高兴度 = 1 + 2 + ... + swap_cnt[i] = swap_cnt[i] * (swap_cnt[i]+1) / 2
// 答案 = 所有小朋友不高兴度的总和
//
// 【为什么用树状数组？——高效计数 "有多少个数 <= x"】
// 暴力做法：对每个 i，扫描前面所有人看谁比他大 → O(n²)，n=10^5 会超时。
// 树状数组做法：
//   把"身高值"当作树状数组的下标，tree[h] 记录"身高恰好为 h 的人出现了几个"。
//   query(h) 返回"身高 <= h 的人一共有几个"（前缀和）。
//   这样就能 O(log MaxH) 地回答"前面有几个人身高 <= 我"。
//
// ============================================================
// 【树状数组 (BIT) 原理简介】
// 树状数组是一种支持 O(log n) 单点修改 + O(log n) 前缀和查询 的数据结构。
// 核心思想：把一个普通数组拆成若干个"区间块"，每个块的长度由下标的 lowbit 决定。
//
//   lowbit(x) = x & (-x)
//   意思是取 x 的二进制最低位的 1。
//   例如：x = 6 = 110₂，lowbit(6) = 010₂ = 2
//         x = 8 = 1000₂，lowbit(8) = 1000₂ = 8
//         x = 12 = 1100₂，lowbit(12) = 0100₂ = 4
//
//   tree[x] 负责管理的区间是 [x - lowbit(x) + 1, x]，长度为 lowbit(x)。
//   例如 tree[6] 管理 [5, 6]，tree[8] 管理 [1, 8]。
//
//   【单点修改 add(idx, val)】
//   从 idx 出发，不断 idx += lowbit(idx)，沿途把所有包含 idx 的块都 +val。
//   例如 add(3, 1)：3 → 4 → 8 → 16 → ...（跳到 >= N 停止）
//
//   【前缀和查询 query(idx)】
//   从 idx 出发，不断 idx -= lowbit(idx)，沿途把所有块累加起来。
//   例如 query(7)：tree[7] + tree[6] + tree[4]
//     7 = 111₂, lowbit=1, 管[7,7]  → 跳到 6
//     6 = 110₂, lowbit=2, 管[5,6]  → 跳到 4
//     4 = 100₂, lowbit=4, 管[1,4]  → 跳到 0，结束
//     合起来 = [1,4] + [5,6] + [7,7] = [1,7] 的总和 ✓
// ============================================================

const int N = 1000005; // 身高最大值 + 偏移，树状数组下标范围
long long tree[N];     // 树状数组，tree[h] 维护 "身高值为 h 的人出现了几个" 的分块前缀和
int h[100005];         // h[i] = 第 i 个小朋友的身高（+1 偏移后的值）
long long swap_cnt[100005]; // swap_cnt[i] = 第 i 个小朋友需要被交换的总次数

// lowbit：取二进制最低位的 1
// 例如 lowbit(6) = lowbit(110₂) = 010₂ = 2
// 原理：-x 是 x 的补码（按位取反 +1），与 x 做 AND 只保留最低位 1
inline int lowbit(int x) {
    return x & -x;
}

// 单点修改：在身高为 idx 的位置，计数 +val
// 沿着 idx, idx+lowbit(idx), idx+2*lowbit(...), ... 依次更新所有覆盖到该点的块
void add(int idx, int val) {
    for (int i = idx; i < N; i += lowbit(i)) {
        tree[i] += val;
    }
}

// 前缀和查询：返回身高在 [1, idx] 范围内的人数总和
// 沿着 idx, idx-lowbit(idx), ... 依次收集所有块的值
long long query(int idx) {
    long long sum = 0;
    for (int i = idx; i > 0; i -= lowbit(i)) {
        sum += tree[i];
    }
    return sum;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    if (!(cin >> n)) return 0;
    
    for (int i = 1; i <= n; ++i) {
        cin >> h[i];
        // 树状数组下标必须 >= 1（lowbit(0) = 0 会死循环），
        // 而题目身高可能为 0，所以全部 +1，不影响相对大小关系
        h[i]++;
    }
    
    // ============================================================
    // 【第一趟：正序扫描，求每个人"前面有几个比他高的"】
    // ============================================================
    // 思路：从左到右逐一把小朋友的身高"插入"树状数组。
    // 处理到第 i 个人时，树状数组里已经有了前面 i-1 个人的身高信息（加上自己共 i 个）。
    //
    // add(h[i], 1)          → 把身高 h[i] 登记进树状数组
    // query(h[i])           → 已插入的人中，身高 <= h[i] 的有多少个
    // i - query(h[i])       → 已插入的 i 个人中，身高 > h[i] 的有多少个
    //                          也就是"前面（含自己那一步之前的人）中比他高的个数"
    //
    // 注意：先 add 再 query 和先 query 再 add 都可以，只要公式对应调整。
    // 这里先 add(h[i], 1)，此时树中已有 i 个人（包括自己），
    // query(h[i]) 返回的 "身高 <= h[i] 的人数" 包含了自己，
    // 所以 "前面比他高的人数" = (i 个人) - query(h[i])
    // 因为 query 包含了自己（身高 == h[i] 的至少有 1 个就是自己），
    // 减去后恰好排除了自己和所有 <= 自己的人，剩下的就是严格比他高的。
    for (int i = 1; i <= n; ++i) {
        add(h[i], 1);
        swap_cnt[i] += i - query(h[i]);
    }
    
    // 清空树状数组，为第二趟做准备
    for (int i = 0; i < N; ++i) tree[i] = 0;
    
    // ============================================================
    // 【第二趟：逆序扫描，求每个人"后面有几个比他矮的"】
    // ============================================================
    // 思路：从右到左逐一把小朋友的身高"插入"树状数组。
    // 处理到第 i 个人时，树状数组里已经有了他后面所有人的身高信息（加上自己）。
    //
    // add(h[i], 1)          → 把身高 h[i] 登记进树状数组
    // query(h[i] - 1)       → 已插入的人中，身高 <= h[i]-1 即身高 < h[i] 的有多少个
    //                          也就是"后面（他右边）比他矮的个数"
    //
    // 注意这里 query 的是 h[i]-1 而不是 h[i]，因为我们要的是"严格比他矮"，
    // 不包括身高相同的人（身高相同不构成逆序对）。
    for (int i = n; i >= 1; --i) {
        add(h[i], 1);
        swap_cnt[i] += query(h[i] - 1);
    }
    
    // ============================================================
    // 【汇总答案】
    // ============================================================
    // swap_cnt[i] = 小朋友 i 被交换的总次数
    // 不高兴度 = 1 + 2 + ... + swap_cnt[i] = swap_cnt[i] * (swap_cnt[i]+1) / 2
    long long ans = 0;
    for (int i = 1; i <= n; ++i) {
        long long k = swap_cnt[i];
        ans += k * (k + 1) / 2;
    }
    
    cout << ans << "\n";
    return 0;
}

