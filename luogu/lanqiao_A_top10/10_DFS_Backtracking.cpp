#include <iostream>
#include <vector>

using namespace std;

// 题型 10：DFS 深度优先搜索与回溯 (蓝桥杯最万能的保底暴力大法)
// 【具体真题】：N 皇后问题 / 全排列 / 组合数求解
// 题目描述：
// 经典的 N 皇后问题：在 N*N 的棋盘上放置 N 个皇后，
// 要求任意两个皇后都不能处于同一行、同一列或同一斜线上。
// 问有多少种不同的摆法？
// (蓝桥杯非常喜欢考 DFS 暴搜，遇到没有思路的题，直接用 DFS 拿 20%~40% 的分)

int n, ans = 0;

// col[i] 记录第 i 列是否被占
// diag1[i] 记录主对角线 (左上到右下，特征是 r-c 为常数) 是否被占，加 n 防止数组越界
// diag2[i] 记录副对角线 (右上到左下，特征是 r+c 为常数) 是否被占
bool col[100], diag1[100], diag2[100];

// dfs 函数：当前正在考虑在第 r 行放皇后
void dfs(int r) {
    // 终止条件：如果成功放到了第 n+1 行，说明前 n 行都已经放好，找到了一种合法方案
    if (r > n) {
        ans++;
        return;
    }
    
    // 尝试在第 r 行的每一列 c 放皇后
    for (int c = 1; c <= n; ++c) {
        // 检查这个位置 (r, c) 是否安全（列、两个对角线都没被占）
        if (!col[c] && !diag1[r - c + n] && !diag2[r + c]) {
            
            // 做出选择：占领这个位置和它的攻击线
            col[c] = diag1[r - c + n] = diag2[r + c] = true;
            
            // 递归进入下一行
            dfs(r + 1);
            
            // 撤销选择（回溯）：拔起皇后，恢复现场，以便在这一行尝试下一个位置
            // 只有恢复了现场，才不会影响其他分支的搜索！
            col[c] = diag1[r - c + n] = diag2[r + c] = false;
        }
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    if (!(cin >> n)) return 0;
    
    dfs(1); // 从第 1 行开始暴搜
    
    cout << ans << "\n"; // 输出合法的摆放方案总数
    
    return 0;
}

