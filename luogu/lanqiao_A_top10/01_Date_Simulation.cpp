#include <iostream>
using namespace std;

// 题型 1：日期与历法问题 (蓝桥杯A组/B组必考送分题)
// 【具体真题】：跑步锻炼 (蓝桥杯 2020 省赛)
// 题目描述：
// 小明从 2000 年 1 月 1 日开始跑步。每天跑 1 千米。
// 规则 1：如果是月初（1 号），他额外多跑 1 千米（即跑 2 千米）。
// 规则 2：如果是星期一，他也额外多跑 1 千米（即跑 2 千米）。
// 规则 3：如果既是月初又是星期一，他不叠加，还是只跑 2 千米。
// 请问从 2000 年 1 月 1 日到 2020 年 10 月 1 日（包含这一天），他一共跑了多少千米？
// （已知 2000 年 1 月 1 日是星期六）

// 答案应为：8879

int days[13] = {0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};

// 判断是否是闰年
bool is_leap(int year) {
    return (year % 4 == 0 && year % 100 != 0) || (year % 400 == 0);
}

int main() {
    int y = 2000, m = 1, d = 1;
    int target_y = 2020, target_m = 10, target_d = 1;
    
    int weekday = 6; // 2000-01-01 是星期六
    int total_km = 0;
    
    // 暴力按天模拟，蓝桥杯最稳的做法
    while (true) {
        // 更新当年 2 月天数
        if (is_leap(y)) days[2] = 29;
        else days[2] = 28;
        
        // 核心判断逻辑
        if (d == 1 || weekday == 1) {
            total_km += 2;
        } else {
            total_km += 1;
        }
        
        // 如果到了目标日期，加完里程后退出循环
        if (y == target_y && m == target_m && d == target_d) {
            break;
        }
        
        // 往后推算一天
        d++;
        weekday++;
        if (weekday > 7) weekday = 1;
        
        // 进位处理
        if (d > days[m]) {
            d = 1;
            m++;
            if (m > 12) {
                m = 1;
                y++;
            }
        }
    }
    
    cout << total_km << "\n";
    return 0;
}

