#include <iostream>
#include <vector>
#include <queue>
#include <algorithm>

using namespace std;
using ll = long long;

// ============================================================
// 题目：任务依赖最长收益链（拓扑排序 + DAG DP）
// ============================================================
//
// 【题目要求】
// 有 n 个任务，每个任务有收益 val[i]。
// 有 m 条依赖关系 u -> v，表示做 v 之前必须先做 u。
// 保证整张图是 DAG（有向无环图）。
// 你可以从任意任务开始，但必须遵守依赖顺序。
// 求一条合法任务链，使得链上任务收益和最大。
//
// 【输入格式】
// 第一行：n m
// 第二行：n 个整数，表示 val[1..n]
// 接下来 m 行：每行两个整数 u v
//
// 【输出格式】
// 输出最大收益。
//
// 【样例输入】
// 5 4
// 3 2 5 4 6
// 1 3
// 2 3
// 3 4
// 3 5
//
// 【样例输出】
// 14
//
// 【为什么这是 DAG DP？】
// 依赖关系保证无环，所以任务可以按拓扑序处理。
// 当我们处理到一个任务 v 时，它所有前驱都已经算完，
// 就可以安全地用这些前驱去更新 v 的最优答案。
//
// 【状态定义】
// dp[i] = 以任务 i 作为链尾时，能获得的最大收益
//
// 初始值：
// 每个任务都可以单独作为起点，所以 dp[i] = val[i]
//
// 转移：
// 对于每条边 u -> v，表示可以从 u 接到 v：
// dp[v] = max(dp[v], dp[u] + val[v])
//
// 最终答案：
// max(dp[1..n])
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    if (!(cin >> n >> m)) return 0;

    vector<ll> val(n + 1);
    for (int i = 1; i <= n; ++i) cin >> val[i];

    vector<vector<int>> adj(n + 1);
    vector<int> indeg(n + 1, 0);

    for (int i = 0; i < m; ++i) {
        int u, v;
        cin >> u >> v;
        adj[u].push_back(v);
        indeg[v]++;
    }

    vector<ll> dp(n + 1, 0);
    queue<int> q;

    // 所有入度为 0 的点都可以作为起点
    for (int i = 1; i <= n; ++i) {
        dp[i] = val[i];
        if (indeg[i] == 0) q.push(i);
    }

    while (!q.empty()) {
        int u = q.front();
        q.pop();

        for (int v : adj[u]) {
            dp[v] = max(dp[v], dp[u] + val[v]);
            indeg[v]--;
            if (indeg[v] == 0) q.push(v);
        }
    }

    ll ans = 0;
    for (int i = 1; i <= n; ++i) ans = max(ans, dp[i]);
    cout << ans << '\n';

    return 0;
}

