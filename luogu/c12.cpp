#include <iostream>
#include <vector>

using namespace std;

// ============================================================
// 题目：左边第一个更小的数（单调栈）
// ============================================================
//
// 【题目要求】
// 给定长度为 n 的序列 a。
// 对每个位置 i，求它左边离它最近、且严格小于 a[i] 的那个数。
// 如果不存在，输出 0。
//
// 【输入格式】
// 第一行：n
// 第二行：n 个整数
//
// 【输出格式】
// 输出 n 个整数，表示每个位置的答案。
//
// 【样例输入】
// 5
// 2 5 3 7 1
//
// 【样例输出】
// 0 2 2 3 0
//
// 【为什么想到单调栈？】
// 题目要求“左边最近满足某种大小关系的元素”，这正是单调栈高频模型。
//
// 我们维护一个“从栈底到栈顶严格递增”的栈。
// 处理当前元素 x=a[i] 时：
// 1. 把所有 >= x 的栈顶弹掉，因为它们不可能成为 x 的答案，
//    也不可能成为后面更小元素的答案；
// 2. 弹完后，如果栈空，说明左边没有更小值；
// 3. 否则栈顶就是“左边离当前位置最近的更小值”；
// 4. 最后把当前元素 x 压栈。
//
// 【为什么栈顶就是最近的？】
// 因为栈里保留下来的元素顺序与原数组顺序一致，
// 而且比当前元素大的都已经被弹掉了，剩下离当前位置最近的自然在栈顶。
//
// 【时间复杂度】
// 每个元素最多入栈一次、出栈一次，总复杂度 O(n)
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    if (!(cin >> n)) return 0;

    vector<int> st; // 单调递增栈，直接存数值即可
    vector<int> ans(n);

    for (int i = 0; i < n; ++i) {
        int x;
        cin >> x;

        while (!st.empty() && st.back() >= x) {
            st.pop_back();
        }

        ans[i] = st.empty() ? 0 : st.back();
        st.push_back(x);
    }

    for (int i = 0; i < n; ++i) {
        cout << ans[i] << (i + 1 == n ? '\n' : ' ');
    }

    return 0;
}

/*
============================================================
【手推样例】
序列：2 5 3 7 1

开始时栈为空

1) 处理 2
   栈空 → 答案 0
   压栈 2
   栈：[2]

2) 处理 5
   栈顶 2 < 5，不弹
   答案是栈顶 2
   压栈 5
   栈：[2,5]

3) 处理 3
   栈顶 5 >= 3，弹出 5
   新栈顶 2 < 3，停止
   答案是 2
   压栈 3
   栈：[2,3]

4) 处理 7
   栈顶 3 < 7，不弹
   答案是 3
   压栈 7
   栈：[2,3,7]

5) 处理 1
   7 >= 1，弹出
   3 >= 1，弹出
   2 >= 1，弹出
   栈空 → 答案 0
   压栈 1
   栈：[1]

最终答案：0 2 2 3 0
============================================================
【易错点总结】
1. 这题要的是“严格小于”，所以弹栈条件是 >= 不是 >。
2. 输出的是“值”，不是位置。
3. 单调栈维护的是候选答案集合，不是排序后的数组。
4. 每个数只进出栈一次，所以是 O(n)，不是 O(n^2)。
============================================================
*/
