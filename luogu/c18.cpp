#include <iostream>
#include <vector>
#include <string>

using namespace std;

// ============================================================
// 题目：最长不含重复字符的子串（双指针 / 滑动窗口）
// ============================================================
//
// 【题目要求】
// 给定一个字符串 s，求其中最长的不含重复字符的连续子串长度。
//
// 【输入格式】
// 一行一个字符串 s
//
// 【输出格式】
// 输出一个整数，表示最长长度。
//
// 【样例输入】
// abcabcbb
//
// 【样例输出】
// 3
//
// 【样例解释】
// 最长的不含重复字符子串可以是 "abc"，长度为 3。
//
// 【为什么想到双指针？】
// 题目要求“连续子串”，并且随着右端点向右扩展，
// 左端点也会单调向右移动，这正是滑动窗口模型。
//
// 我们维护一个窗口 [l, r]，表示当前这段子串内没有重复字符。
// 当右端加入新字符 s[r] 时：
// - 如果没有重复，窗口继续扩大；
// - 如果出现重复，就不断右移 l，把左边字符移出窗口，直到恢复合法。
//
// 【状态维护】
// cnt[ch] = 当前窗口内字符 ch 出现了多少次
//
// 这样每加入一个字符，只要发现 cnt[s[r]] > 1，
// 就说明窗口里出现了重复字符，需要收缩左边界。
//
// 【时间复杂度】
// 左右指针都只会从左到右移动一次，总复杂度 O(n)
// ============================================================

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    string s;
    if (!(cin >> s)) return 0;

    vector<int> cnt(256, 0);
    int ans = 0;

    for (int l = 0, r = 0; r < (int)s.size(); ++r) {
        cnt[(unsigned char)s[r]]++;

        // 如果当前字符重复了，就不断移动左端点，直到窗口重新合法
        while (cnt[(unsigned char)s[r]] > 1) {
            cnt[(unsigned char)s[l]]--;
            l++;
        }

        ans = max(ans, r - l + 1);
    }

    cout << ans << '\n';
    return 0;
}

/*
============================================================
【手推样例】
s = "abcabcbb"

开始：l=0

r=0，加入 'a'
窗口 = "a"，无重复，长度 1

r=1，加入 'b'
窗口 = "ab"，无重复，长度 2

r=2，加入 'c'
窗口 = "abc"，无重复，长度 3

r=3，加入 'a'
此时 'a' 重复了，开始移动 l：
- 去掉 s[0]='a'
窗口重新合法，此时窗口是 "bca"
长度还是 3

继续这样推进，最终最大长度始终为 3。
============================================================
【易错点总结】
1. 这题求的是“连续子串”，不是子序列。
2. while 收缩窗口时，要减掉 s[l] 的出现次数，再 l++。
3. 统计数组常开成 256，兼容常见字符输入。
4. 双指针能做的关键，是左指针不会回退，所以总复杂度是 O(n)。
============================================================
*/
