#include "coins_min.h"
#include <algorithm>
#include <climits>

namespace coins_min {

    // ========================================================================
    // process: 暴力递归核心函数
    // ========================================================================
    // 【功能】使用 arr[index...] 位置的硬币面值，凑成 rest 金额的最少硬币数
    //        每种面值的硬币可以无限使用！
    //
    // 【参数说明】
    //   - arr: 硬币面值数组（每种面值可无限使用）
    //   - index: 当前考虑的硬币面值索引
    //   - rest: 剩余需要凑的金额
    //
    // 【返回值】
    //   - 返回最少硬币数
    //   - 返回 -1 表示无法凑成
    //
    // 【递归思路图解】
    //   
    //   假设 arr = [2, 3, 5], rest = 7
    //   
    //   从 index=0 开始，对于面值 arr[index]，可以选择用 0, 1, 2, ... 枚：
    //   
    //                      process(0, 7)
    //                     /      |       \
    //               用0枚2    用1枚2    用2枚2 ...
    //              process(1,7) process(1,5) process(1,3)
    //
    //   最终找到最少硬币数的路径
    //
    // ========================================================================
    static int process(const std::vector<int>& arr, int index, int rest) {
        // ============================================================
        // 基本情况：已经没有硬币面值可选了
        // ============================================================
        if (index == static_cast<int>(arr.size())) {
            // 如果恰好凑够了（rest == 0），返回 0
            // 否则无法凑成，返回 -1
            return rest == 0 ? 0 : -1;
        }

        // ============================================================
        // 递归情况：尝试使用当前面值 0, 1, 2, ... 枚硬币
        // ============================================================
        // 枚举当前面值 arr[index] 使用的数量
        // zhang: 使用的硬币数量
        // zhang * arr[index]: 使用这些硬币消耗的金额
        // ============================================================
        int minCoins = -1;  // 记录最少硬币数，-1 表示还没找到可行方案
        
        for (int zhang = 0; zhang * arr[index] <= rest; ++zhang) {
            // 使用 zhang 枚面值为 arr[index] 的硬币后
            // 剩余金额为 rest - zhang * arr[index]
            // 递归处理剩余面值
            int next = process(arr, index + 1, rest - zhang * arr[index]);
            
            // 如果这条路可行（next != -1）
            if (next != -1) {
                int total = zhang + next;  // 总硬币数 = 当前用的 + 后续用的
                if (minCoins == -1 || total < minCoins) {
                    minCoins = total;
                }
            }
        }
        
        return minCoins;
    }

    // ========================================================================
    // minCoinsRecursive: 暴力递归入口函数
    // ========================================================================
    // 直接调用 process，从第 0 个硬币开始，凑成 amount 金额
    // ========================================================================
    int minCoinsRecursive(const std::vector<int>& arr, int amount) {
        if (arr.empty() || amount < 0) {
            return -1;
        }
        if (amount == 0) {
            return 0;
        }
        return process(arr, 0, amount);
    }

    // ========================================================================
    // minCoinsDp: 动态规划方法
    // ========================================================================
    // 【核心思想】
    //   将递归改为填表，避免重复计算相同的子问题。
    //   每种面值的硬币可以无限使用！
    //
    // 【状态定义】
    //   dp[i][j] = 用 arr[i...] 的硬币面值凑成金额 j 的最少硬币数
    //             （-1 表示无法凑成）
    //
    // 【状态转移】
    //   与递归版本完全对应：
    //   - dp[n][0] = 0        （不需要凑钱，用 0 枚硬币）
    //   - dp[n][j] = -1       （j > 0 时，没有硬币面值可用，无法凑成）
    //   - dp[i][0] = 0        （已凑够，不需要更多硬币）
    //   - dp[i][j] = min 枚举 zhang 从 0 到 j/arr[i]
    //               { zhang + dp[i+1][j - zhang*arr[i]] }
    //
    // 【填表顺序】
    //   从 index = n-1 往 0 填，因为 dp[i] 依赖 dp[i+1]
    //
    // 【图示】
    //   假设 arr = [1, 2, 5], amount = 11
    //   
    //   dp 表格填写方式：
    //   对于每个 dp[index][rest]，枚举使用 0,1,2,... 枚 arr[index] 硬币
    //   取所有可行方案中的最小值
    //   
    //   答案 = dp[0][11] = 3（5+5+1）
    //
    // ========================================================================
    int minCoinsDp(const std::vector<int>& arr, int amount) {
        // 边界检查
        if (arr.empty() || amount < 0) {
            return -1;
        }
        if (amount == 0) {
            return 0;
        }

        int n = static_cast<int>(arr.size());
        
        // ============================================================
        // 创建 dp 表格
        // ============================================================
        // dp[i][j] 表示用 arr[i...] 凑成金额 j 的最少硬币数
        // 初始化为 -1（表示"尚未计算"或"无法凑成"）
        std::vector<std::vector<int>> dp(n + 1, std::vector<int>(amount + 1, -1));

        // ============================================================
        // 基本情况初始化
        // ============================================================
        // 当 rest = 0 时，无论从哪个 index 开始，都只需要 0 枚硬币
        for (int i = 0; i <= n; ++i) {
            dp[i][0] = 0;
        }
        // 当 index = n 时（没有硬币面值可用），除了 rest=0 外都是 -1
        // 这在初始化时已经设置好了（整个表初始化为 -1）

        // ============================================================
        // 从后往前填表
        // ============================================================
        // 从 index = n-1 开始，因为 dp[i] 依赖于 dp[i+1]
        for (int index = n - 1; index >= 0; --index) {
            for (int rest = 1; rest <= amount; ++rest) {
                
                // 枚举当前面值 arr[index] 使用 zhang 枚 (0, 1, 2, ...)
                int minCoins = -1;
                for (int zhang = 0; zhang * arr[index] <= rest; ++zhang) {
                    int next = dp[index + 1][rest - zhang * arr[index]];
                    if (next != -1) {
                        int total = zhang + next;
                        if (minCoins == -1 || total < minCoins) {
                            minCoins = total;
                        }
                    }
                }
                dp[index][rest] = minCoins;
            }
        }

        // ============================================================
        // 返回最终答案
        // ============================================================
        // dp[0][amount] 就是用所有硬币面值凑成 amount 的最少硬币数
        return dp[0][amount];
    }

}
